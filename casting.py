# Specify a Variable Type
# int()
# float()
# str()

# integers

x = int(2)
y = int(2.8)
z = int('3')

print(x)
print(type(x))

print(y)
print(type(y))

print(z)
print(type(z))


# floats
a = float(2)
print(a)
print(type(a))

b = float(4.5)
print(b)
print(type(b))

c = float("5.6")
print(c)
print(type(c))

d = float("3")
print(d)
print(type(d))

# strings

x = str("s1")
print(x)
print(type(x))


y = str(3)
print(y)
print(type(y))

z = str(3.0)
print(z)
print(type(z))

last = str(45)
print(last)
print(type(last))


